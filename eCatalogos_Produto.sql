SELECT 
	B4.B4_COD,
	B1.B1_COD,
	(
		SELECT 
			BV.BV_DESCRI 
		FROM 
			SBV010 BV 
		WHERE 
			BV.BV_FILIAL = '' AND 
			BV.BV_TABELA = B4.B4_COLUNA AND 
			BV.BV_CHAVE = B1.B1_01CLGRD AND 
			BV.D_E_L_E_T_ = ''
	) TAMANHO,
	(SELECT BV.BV_DESCRI FROM SBV010 BV WHERE BV.BV_FILIAL = '' AND BV.BV_TABELA = B4.B4_LINHA AND BV.BV_CHAVE = B1.B1_01LNGRD AND BV.D_E_L_E_T_ = '') COR,
	B4.B4_DESC,
	B4.B4_01CAT1,
	B4.B4_01CAT2,
	B4.B4_01CAT3,
	B4.B4_01CAT4,
	B4.B4_01CAT5,
	B4.B4_01COLEC,
	B4.B4_PICM,
	B4.B4_PESO,
	B1.B1_PESO,
	B1.B1_CODBAR,
	B4.R_E_C_N_O_ RECNOSB4,
	DA1.DA1_PRCVEN,
	Y2.AY2_DESCR,
	Y2.AY2_LOCAL
FROM
	DA1010 DA1 
	INNER JOIN DA0010 DA0 ON DA0.DA0_FILIAL = '06' AND DA0.DA0_CODTAB = '027' AND DA0.D_E_L_E_T_ = ''
	INNER JOIN SB4010 B4 ON B4.B4_FILIAL = '' AND B4.B4_COD = DA1.DA1_REFGRD AND B4.B4_STATUS = 'A' AND B4.D_E_L_E_T_ = ''
	INNER JOIN AY2010 Y2 ON Y2.AY2_FILIAL = '' AND Y2.AY2_CODIGO = B4.B4_01CODMA AND Y2.D_E_L_E_T_ = ''
	INNER JOIN SB1010 B1 ON B1.B1_FILIAL = '' AND SUBSTRING(B1.B1_COD,1,6) = B4.B4_COD AND B1.D_E_L_E_T_ = ''
WHERE
	DA1.DA1_FILIAL = '06' AND 
	DA1.DA1_CODTAB  = '027' AND 
	DA1.D_E_L_E_T_ = ''
ORDER BY DA1.DA1_REFGRD





SELECT 
	AY5.AY5_CODIGO,
	AY5.AY5_SEQ,
	CAST(CAST(AY4.AY4_VLRECO AS BINARY) AS VARCHAR) VLRECO, 
	AY5.AY5_CODPRO,
	AY5.AY5_STATUS,
	AY5.R_E_C_N_O_ RECNOAY5, 
	AY4.R_E_C_N_O_ RECNOAY4 
FROM 
	AY5010 AY5 
	INNER JOIN AY4010 AY4 ON AY4.AY4_FILIAL = '' AND AY4.AY4_CODCAR = AY5.AY5_CODIGO AND AY4.AY4_SEQ = AY5.AY5_SEQ AND AY4.AY4_ENVECO = '1' AND AY4.D_E_L_E_T_ = '' 
	INNER JOIN SB4010 B4 ON B4.B4_FILIAL = '' AND B4.B4_COD = AY5.AY5_CODPRO AND B4.B4_XENVECO = '2' AND  B4.B4_XUSAECO = 'S' AND B4.D_E_L_E_T_ = '' 
WHERE
	AY5.AY5_FILIAL = '' AND 
	AY5.AY5_ENVECO = '1' AND  
	AY5.D_E_L_E_T_ = ' ' 
ORDER BY AY5_CODPRO 



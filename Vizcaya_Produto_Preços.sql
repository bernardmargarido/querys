SELECT 
	B1.B1_XIDSKU CODPRD, 
	B1.B1_DESC DESCPRD, 
	B1.B1_PRV1 PRCCHEIO, 
	B1.B1_XIDLOJA,
	DA0.DA0_DATDE,
	DA0.DA0_HORADE,
	DA1.DA1_PRCVEN PRCPOR, 
	DA1.DA1_CODTAB CODTAB,
	DA1.DA1_ITEM ITEMTAB,
	DA1.R_E_C_N_O_ RECNODA1 
FROM 
		DA1010 DA1
		INNER JOIN DA0010 DA0 ON DA0.DA0_FILIAL = '' AND DA0.DA0_CODTAB = DA1.DA1_CODTAB AND DA0.D_E_L_E_T_ = ''
		INNER JOIN SB1010 B1 ON B1.B1_FILIAL = '  ' AND B1.B1_COD = DA1.DA1_CODPRO AND B1.B1_XENVECO = '2' AND B1.B1_XUSAECO = 'S' AND B1.B1_MSBLQL <> '1' AND B1.D_E_L_E_T_ = '' 
WHERE 
		DA1.DA1_FILIAL = '  ' AND  
		DA1.DA1_CODTAB = 'ECO' AND 
		DA1.DA1_ENVECO = '1' AND  
		DA1.D_E_L_E_T_ = '' 
ORDER BY B1.B1_COD 

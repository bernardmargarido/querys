	B5.B5_COD,
	B5.B5_XNOMPRD,
	ISNULL(CAST(CAST(B5.B5_XDESCRI AS BINARY(2048)) AS VARCHAR(2048)),'') DESCECO, 
	ISNULL(CAST(CAST(B5.B5_XCARACT AS BINARY(2048)) AS VARCHAR(2048)),'') DESCCARA,
	ISNULL(CAST(CAST(B5.B5_XKEYWOR AS BINARY(2048)) AS VARCHAR(2048)),'') KEYWORDS,
	ISNULL( ( SELECT AY0.AY0_IDECOM FROM AY0040 AY0 WHERE AY0.AY0_CODIGO = B5.B5_XCAT01 AND AY0.D_E_L_E_T_ = '' ),'') IDCAT01,
	ISNULL( ( SELECT AY0.AY0_IDECOM FROM AY0040 AY0 WHERE AY0.AY0_CODIGO = B5.B5_XCAT02 AND AY0.D_E_L_E_T_ = '' ),'') IDCAT02,
	ISNULL( ( SELECT AY0.AY0_IDECOM FROM AY0040 AY0 WHERE AY0.AY0_CODIGO = B5.B5_XCAT03 AND AY0.D_E_L_E_T_ = '' ),'') IDCAT03,
	ISNULL( ( SELECT AY0.AY0_IDECOM FROM AY0040 AY0 WHERE AY0.AY0_CODIGO = B5.B5_XCAT04 AND AY0.D_E_L_E_T_ = '' ),'') IDCAT04,
	ISNULL( ( SELECT AY0.AY0_IDECOM FROM AY0040 AY0 WHERE AY0.AY0_CODIGO = B5.B5_XCAT05 AND AY0.D_E_L_E_T_ = '' ),'') IDCAT05,
	ISNULL( ( SELECT CAST(CAST(AY2.AY2_DESECO AS BINARY(2048)) AS VARCHAR(2048)) FROM AY2040 AY2 WHERE AY2.AY2_CODIGO = B5.B5_XCODMAR AND AY2.D_E_L_E_T_ = '' ),'') DESCMARCA,
	B5.B5_XSTAPRD,
	B5.B5_XIDPROD,
	B5.B5_XIDSKU,
	B5.B5_XALTPRD,
	B5.B5_XALTEMB,
	B5.B5_XLARPRD,
	B5.B5_XLAREMB,
	B5.B5_XPROPRD,
	B5.B5_XPROEMB,
	B1.B1_PESO,
	B1.B1_PESBRU,
	B1.B1_XPESCUB,
	B5.R_E_C_N_O_ RECNOSB5
FROM
	SB5040 B5
	INNER JOIN SB1040 B1 ON B1.B1_FILIAL = '    ' AND B1.B1_COD = B5.B5_COD AND B1.D_E_L_E_T_ = ''
WHERE
	B5.B5_FILIAL = '' AND 
	B5.B5_XUSAECO = 'S' AND 
	B5.B5_XENVECO = '1' AND 
	B5.B5_XENVSKU = '1' AND 
	B5.D_E_L_E_T_ = ''
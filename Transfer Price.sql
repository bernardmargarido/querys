SELECT
	SD2.D2_COD PRODUTO, SD2.D2_DOC NOTA, SD2.D2_SERIE SERIE, SD2.D2_EMISSAO DTAEMISS,
	SUM( SD2.D2_QUANT ) QUANTIDADE, SUM( SD2.D2_PRCVEN ) PRECOVENDA, 
	SUM( SD2.D2_TOTAL ) TOTAL, SUM( SD2.D2_DESCON ) DESCONTO, 
	SUM( SD2.D2_VALICM ) ICMS, SUM( SD2.D2_VALIMP6 ) PIS, 
	SUM( SD2.D2_VALIMP5 ) COFINS, SUM( SD2.D2_COMIS1 ) COMISSAO, 
	( SUM( SD2.D2_PRCVEN ) - SUM( SD2.D2_DESCON ) ) * 0.2  MARGEM 
FROM SD2010 SD2 
INNER JOIN SB1010 SB1 ON SB1.B1_FILIAL = '' AND SB1.B1_COD = SD2.D2_COD AND SB1.B1_ORIGEM = '1' AND SB1.D_E_L_E_T_ = ''
WHERE 
	SD2.D2_FILIAL = '05'  AND 
	SD2.D2_COD BETWEEN '' AND  'ZZZZZZZZZZZZZZZZZZZZZZZZ' AND 
	SD2.D2_EMISSAO BETWEEN '20130101' AND '20131231' AND 
	SD2.D2_CF IN( '5102', '6102', '6108', '6109', '6110', '5403', '6403', '5405', '6405' ) AND 
	SD2.D_E_L_E_T_ = ' ' 
GROUP BY SD2.D2_COD,SD2.D2_DOC,SD2.D2_SERIE,SD2.D2_EMISSAO 
ORDER BY SD2.D2_COD,SD2.D2_DOC,SD2.D2_SERIE 
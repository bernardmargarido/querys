SELECT 
SC5.C5_NUM,SC5.C5_CLIENTE,SC5.C5_LOJACLI,
SC5.C5_VEND1,SC6.C6_PRODUTO,SC6.C6_DESCRI,
SC6.C6_QTDVEN,SC6.C6_QTDEMP, SC6.C6_QTDENT,
SC6.C6_PRCVEN,SC6.C6_TES,
(	SELECT 
		SUM(C9_QTDLIB) QTDLIB
	FROM 
		SC9010 
	WHERE 
		C9_FILIAL = SC6.C6_FILIAL AND 
		C9_PEDIDO = SC6.C6_NUM AND 
		C9_PRODUTO = SC6.C6_PRODUTO AND 
		C9_NFISCAL = '' AND 
		D_E_L_E_T_ = ''
) QTDLIB
FROM 
SC5010 SC5

INNER JOIN SC6010 SC6 ON SC6.C6_FILIAL = SC5.C5_FILIAL AND SC6.C6_NUM = SC5.C5_NUM AND 
SC6.C6_BLQ <> 'R' AND SC6.C6_ENTREG BETWEEN '20000101' AND '20131231' AND 
SC6.C6_PRODUTO BETWEEN '' AND 'ZZZZZZZZZZZZZZZZZZZZZZZZZZZZZZ' AND SC6.D_E_L_E_T_ = ''

WHERE
SC5.C5_FILIAL = '02' AND
SC5.C5_NUM BETWEEN '' AND 'ZZZZZZ' AND 
SC5.C5_CLIENTE BETWEEN '' AND 'ZZZZZZ' AND 
SC5.C5_LOJACLI BETWEEN '' AND 'ZZ' AND 
SC5.C5_VEND1 BETWEEN '000003'  AND '000003' AND 
SC5.D_E_L_E_T_ = ''
ORDER BY SC5.C5_NUM
/*
GROUP BY SC5.C5_NUM,SC5.C5_CLIENTE,SC5.C5_LOJACLI,
SC5.C5_VEND1,SC6.C6_PRODUTO,SC6.C6_DESCRI,
SC6.C6_QTDVEN,SC6.C6_QTDEMP,SC6.C6_QTDENT,
SC6.C6_PRCVEN,SC6.C6_TES,SC6.C6_ENTREG
*/

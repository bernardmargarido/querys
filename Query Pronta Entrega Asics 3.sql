SELECT 
SC5.C5_NUM,SC5.C5_CLIENTE,SC5.C5_LOJACLI, 
SC5.C5_TIPO,SC5.C5_TRANSP,SC5.C5_EMISSAO,  	 
SC5.C5_TIPOCLI,SC5.C5_CONDPAG,SC5.C5_VEND1, 
SC6.C6_ITEM,SC6.C6_PRODUTO,SC6.C6_DESCRI, 
SC6.C6_XCANC,SC6.C6_QTDVEN,SC6.C6_QTDEMP, 
SC6.C6_QTDENT,SC6.C6_ENTREG,SC6.C6_LOCAL, 
SC6.C6_VALOR,SC6.C6_VALDESC,SC6.C6_PRCVEN, 
SC6.C6_TES, 	
(	SELECT 
		SUM(C9_QTDLIB) QTDLIB 
	FROM 
		SC9010 
	WHERE 
		C9_FILIAL = SC6.C6_FILIAL AND 
		C9_PEDIDO = SC6.C6_NUM AND 
		C9_PRODUTO = SC6.C6_PRODUTO AND 
		C9_NFISCAL = '' AND  
		C9_BLCRED = ''	AND 
		C9_BLEST = '' AND  
		D_E_L_E_T_ = '' 
) QTDLIB, 
(	SELECT  
		SUM(C9_QTDLIB) QTDBLQ  
	FROM  
		SC9010
	WHERE  
		C9_FILIAL = SC6.C6_FILIAL AND  
		C9_PEDIDO = SC6.C6_NUM AND  
		C9_PRODUTO = SC6.C6_PRODUTO AND  
		C9_NFISCAL = '' AND  
		C9_BLCRED <> ''	AND 
		C9_BLEST <> '' AND  
		D_E_L_E_T_ = ''  
) QTDBLQ  
FROM 
SC5010 SC5 

INNER JOIN SC6010 SC6 ON SC6.C6_FILIAL = SC5.C5_FILIAL AND SC6.C6_NUM = SC5.C5_NUM AND 
SC6.C6_BLQ <> 'R' AND SC6.C6_ENTREG BETWEEN '20130101' AND '20130314' AND 
SC6.C6_PRODUTO BETWEEN '' AND 'ZZZZZZZZZZZZZZZZZZZZZ' AND SC6.D_E_L_E_T_ = '' 

WHERE
SC5.C5_FILIAL = '05' AND 
SC5.C5_NUM BETWEEN '' AND 'ZZZZZZ' AND  
SC5.C5_CLIENTE BETWEEN '' AND 'ZZZZZZZZ' AND 
SC5.C5_LOJACLI BETWEEN '' AND 'ZZ' AND 
SC5.C5_VEND1 BETWEEN '000001'  AND '000001' AND 
SC5.D_E_L_E_T_ = '' 
ORDER BY C5_NUM,C5_VEND1,C6_ITEM,C6_PRODUTO
SELECT C5_NUM, C5_EMISSAO, C5_VEND1,A3_NOME, C6_NOTA, C6_DATFAT, 
C5_TRANSP, A4_NOME, C5_DESC1, C6_PRODUTO, C6_QTDVEN, C6_PRCVEN, C6_VALOR ,
D2_IPI,D2_VALIPI,D2_PICM,D2_VALICM,D2_ICMSRET,F4_CF,F4_TEXTO
FROM SC5010 SC5 
INNER JOIN SC6010 SC6 ON SC6.C6_FILIAL = SC5.C5_FILIAL AND SC6.C6_NUM = SC5.C5_NUM AND SC6.C6_LOJA = '01' AND (C6_DATFAT IS NOT NULL) AND SC6.D_E_L_E_T_ = ' '
INNER JOIN SF2010 SF2 ON SF2.F2_FILIAL = SC5.C5_FILIAL AND SF2.F2_DOC = SC5.C5_NOTA AND SF2.F2_SERIE = SC5.C5_SERIE AND SF2.D_E_L_E_T_ = ' '
INNER JOIN SD2010 SD2 ON SD2.D2_FILIAL = SC5.C5_FILIAL AND SD2.D2_DOC = SC5.C5_NOTA AND SD2.D2_SERIE = SC5.C5_SERIE AND SD2.D_E_L_E_T_ = ' '
INNER JOIN SF4010 SF4 ON SF4.F4_CODIGO = SD2.D2_TES AND SF4.D_E_L_E_T_ = ' '
LEFT OUTER JOIN SA3010 SA3 ON SA3.A3_COD = SC5.C5_VEND1 AND SA3.D_E_L_E_T_ = ' '
LEFT OUTER JOIN SA4010 SA4 ON SA4.A4_COD = SC5.C5_TRANSP AND SA4.D_E_L_E_T_ = ' '
WHERE
SC5.C5_NOTA <> ' ' AND 
SC5.C5_SERIE <> ' '  AND   
--SC5.C5_CLIENTE = ' ' AND 
SC5.C5_NUM = '003365' AND
SC5.D_E_L_E_T_ = ' ' 
--GROUP BY C5_NUM, C5_EMISSAO, C5_VEND1, A3_NOME, C6_NOTA, C6_DATFAT, C5_TRANSP, A4_NOME, C5_DESC1 
ORDER BY C5_EMISSAO


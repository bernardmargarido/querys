SELECT 
	CODSKU , 
	IDSKU , 
	DESCSKU, 
	PRCDE , 
	IDLOJA, 
	DATADE,
	HORADE,
	PRCPOR, 
	CODTABELA, 
	ITEM, 
	RECNODA1 
FROM
(
	SELECT 
		B5.B5_COD CODSKU , 
		B5.B5_XIDSKU IDSKU , 
		B1.B1_DESC DESCSKU, 
		B1.B1_PRV1 PRCDE , 
		B5.B5_XIDLOJA IDLOJA, 
		DA0.DA0_DATDE DATADE,
		DA0.DA0_HORADE HORADE,
		DA1.DA1_PRCVEN PRCPOR, 
		DA1.DA1_CODTAB CODTABELA, 
		DA1.DA1_ITEM ITEM , 
		DA1.R_E_C_N_O_ RECNODA1 
	FROM 
		DA1010 DA1 
		INNER JOIN DA0010 DA0 ON DA0.DA0_FILIAL = DA1.DA1_FILIAL AND DA0.DA0_CODTAB = DA1.DA1_CODTAB AND DA0.D_E_L_E_T_ = ' ' 
		INNER JOIN SB5010 B5 ON B5.B5_FILIAL = '0101' AND B5.B5_COD = DA1.DA1_CODPRO AND B5.B5_XENVECO = '2' AND B5.B5_XENVSKU = '2' AND B5.B5_XUSAECO = 'S' AND B5.D_E_L_E_T_ = '' 
		INNER JOIN SB1010 B1 ON B1.B1_FILIAL = '0101' AND B1.B1_COD = DA1.DA1_CODPRO AND B1.B1_MSBLQL <> '1' AND B1.D_E_L_E_T_ = '' 
	WHERE 
		DA1.DA1_FILIAL = '01  ' AND 
		DA1.DA1_CODTAB = '001' AND 
		DA1.DA1_ENVECO = '1' AND  
		DA1.D_E_L_E_T_ = '' 

	UNION 

	SELECT 
		WS6.WS6_CODSKU CODSKU , 
		WS6.WS6_IDSKU IDSKU , 
		B1.B1_DESC DESCSKU, 
		B1.B1_PRV1 PRCDE , 
		WS5.WS5_IDLOJA IDLOJA, 
		DA0.DA0_DATDE DATADE,
		DA0.DA0_HORADE HORADE,
		DA1.DA1_PRCVEN PRCPOR, 
		DA1.DA1_CODTAB CODTABELA, 
		DA1.DA1_ITEM ITEM , 
		DA1.R_E_C_N_O_ RECNODA1 
	FROM 
		DA1010 DA1 
		INNER JOIN DA0010 DA0 ON DA0.DA0_FILIAL = DA1.DA1_FILIAL AND DA0.DA0_CODTAB = DA1.DA1_CODTAB AND DA0.D_E_L_E_T_ = ' ' 
		INNER JOIN WS6010 WS6 ON WS6.WS6_FILIAL = '' AND WS6.WS6_CODSKU = DA1.DA1_CODPRO AND WS6.D_E_L_E_T_ = '' 
		INNER JOIN WS5010 WS5 ON WS5.WS5_FILIAL = '' AND WS5.WS5_CODPRD = WS6.WS6_CODPRD AND WS5.WS5_ENVECO ='2' AND WS5.WS5_USAECO = 'S' AND WS5.D_E_L_E_T_ = '' 
		INNER JOIN SB1010 B1 ON B1.B1_FILIAL = '0101' AND B1.B1_COD = DA1.DA1_CODPRO AND B1.B1_MSBLQL <> '1' AND B1.D_E_L_E_T_ = '' 
	WHERE 
		DA1.DA1_FILIAL = '01  ' AND 
		DA1.DA1_CODTAB = '001' AND 
		DA1.DA1_ENVECO = '1' AND  
		DA1.D_E_L_E_T_ = '' 
) PRECO
ORDER BY CODSKU


SELECT 
	(VALOR_RECEBER_ATUAL + VALOR_RECEBIDO_MES) SALDO_TOTAL 
FROM 
(

	SELECT 
		SUM(E1.E1_SALDO) VALOR_RECEBER_ATUAL,
		(
			SELECT 
				SUM(E1A.E1_VALLIQ)
			FROM 
				SE1040 E1A WITH (NOLOCK)
			WHERE 
				E1A.E1_FILIAL = '0404' AND 
				E1A.E1_EMISSAO <= '20210531' AND 
				E1A.E1_BAIXA BETWEEN '20210501' AND '20210531' AND
				E1A.D_E_L_E_T_ = ''
		) VALOR_RECEBIDO_MES 
	FROM 
		SE1040 E1 WITH (NOLOCK) 
	WHERE 
		E1.E1_FILIAL = '0404' AND 
		E1.E1_EMISSAO <= '20210430' AND 
		( E1.E1_BAIXA = '' OR E1.E1_SALDO > 0 ) AND
		E1.D_E_L_E_T_ = ''
)SALDO_TOTAL
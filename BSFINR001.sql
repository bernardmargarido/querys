SELECT 
	C5.C5_FILIAL,
	C5.C5_NUM,
	C5.C5_EMISSAO,
	C5.C5_XVALOR,
	C5.C5_XNUMECO,
	A1.A1_NOME,
	A1.A1_NREDUZ,
	A1.A1_CGC,
	A1.A1_COD,
	A1.A1_LOJA,
	A1.A1_LC,
	A1.A1_VENCLC,
	A1.A1_SALDUP,
	A1.A1_ATR,
	A3.A3_COD,
	A3.A3_NREDUZ,
	C9.C9_DATALIB,
	E4.E4_CODIGO,
	E4.E4_DESCRI,
	CASE 
		WHEN Z05.Z05_STATUS <> '' THEN 
			'MANUAL' 
		ELSE
			'AUTOMATICO'
	END Z05_STATUS,
	COALESCE(Z05.Z05_NOMUSU,'') Z05_NOMUSU
FROM 
	SC5040 C5 
	INNER JOIN SA1040 A1 ON A1.A1_FILIAL = '    ' AND A1.A1_COD = C5.C5_CLIENT AND A1.A1_LOJA = C5.C5_LOJACLI AND A1.D_E_L_E_T_ = ''
	INNER JOIN SC9040 C9 ON C9.C9_FILIAL = C5.C5_FILIAL AND C9.C9_PEDIDO = C5.C5_NUM AND C9.C9_NFISCAL = '' AND C9.C9_SERIENF = '' AND C9.D_E_L_E_T_ = ''
	INNER JOIN SE4040 E4 ON E4.E4_FILIAL = '    ' AND E4.E4_CODIGO = C5.C5_CONDPAG AND E4.D_E_L_E_T_ = ''
	INNER JOIN SA3040 A3 ON A3.A3_FILIAL = '    ' AND A3.A3_COD = C5.C5_VEND1 AND A3.D_E_L_E_T_ = ''
	LEFT JOIN Z05040 Z05 ON Z05.Z05_FILIAL = C5.C5_FILIAL AND Z05.Z05_PEDIDO = C5.C5_NUM AND Z05.Z05_STATUS IN('000036') AND Z05.D_E_L_E_T_ = ''
WHERE 
	C5.C5_FILIAL BETWEEN '' AND 'ZZZZ' AND 
	C5.C5_NUM BETWEEN '' AND 'ZZZZZZ' AND 
	C5.C5_EMISSAO BETWEEN '20200101' AND '20200131' AND 
	C5.C5_TIPO = 'N' AND 
	C5.D_E_L_E_T_ = ''
GROUP BY C5.C5_FILIAL,C5.C5_NUM,C5.C5_EMISSAO,C5.C5_XVALOR,C5.C5_XNUMECO,A1.A1_NOME,A1.A1_NREDUZ,A1.A1_CGC,A1.A1_COD,A1.A1_LOJA,A1.A1_LC,A1.A1_VENCLC,A1.A1_SALDUP,A1.A1_ATR,A3.A3_COD,A3.A3_NREDUZ,C9.C9_DATALIB,E4.E4_CODIGO,E4.E4_DESCRI,Z05.Z05_STATUS,Z05.Z05_NOMUSU
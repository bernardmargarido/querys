 SELECT 
 	SC9.C9_PEDIDO, 
 	SC9.C9_ITEM, 
 	SC9.C9_PRODUTO, 
 	SC9.C9_SEQUEN, 
 	SC9.C9_DATALIB, 
 	SC9.C9_01DTFAT, 
 	SC9.C9_CLIENTE, 
 	SC9.C9_LOJA, 
 	SA1.A1_NREDUZ, 
 	SA1.A1_01AGEND, 
 	SC9.C9_01SEQ, 
 	SC9.C9_01PEDOP, 
 	SC9.C9_QTDLIB, 
 	SC9.C9_01PACKS, 
 	SC9.C9_01GRADE, 
 	SC9.C9_GRUPO, 
 	SC9.C9_LOCAL, 
 	SC9.C9_PRCVEN 
 FROM SC9010 SC9 
 INNER JOIN SC6010 SC6 
 	ON SC6.C6_FILIAL = SC9.C9_FILIAL 
 	AND SC6.C6_NUM = SC9.C9_PEDIDO 
 	AND SC6.C6_PRODUTO = SC9.C9_PRODUTO 
 	AND SC6.C6_ITEM = SC9.C9_ITEM 
 	AND SC6.C6_TES <> '' 
 	AND SC6.C6_BLQ <> 'R' 
 	AND SC6.D_E_L_E_T_ = '' 
 INNER JOIN SC5010 SC5 
 	ON SC5.C5_FILIAL = SC9.C9_FILIAL 
 	AND SC5.C5_NUM = SC9.C9_PEDIDO 
 	AND SC5.C5_TIPO IN ('N','D','B') 
 	AND SC5.D_E_L_E_T_ = '' 
 INNER JOIN SF4010 SF4 
 	ON SF4.F4_FILIAL = '05' 
 	AND SF4.F4_CODIGO = SC6.C6_TES 
 	AND SF4.F4_ESTOQUE = 'S' 
 	AND SF4.D_E_L_E_T_ = '' 
 INNER JOIN SA1010 SA1 
 	ON SA1.A1_FILIAL = '  ' 
 	AND SA1.A1_COD = SC9.C9_CLIENTE 
 	AND SA1.A1_LOJA = SC9.C9_LOJA 
 	AND SA1.D_E_L_E_T_ = '' 
 WHERE 
 	SC9.C9_FILIAL = '05' 
 	AND SC9.C9_01DTFAT <= '20140328' 
 	AND SC9.C9_GRUPO IN ('100','200','300')
 	AND SC9.C9_LOCAL IN ('01','02','03','04','05','12','13','14','Y1','Y2','X1','X2','X3','Y3','Z1','M1','M2','M3','W1','W2','W3')
 	AND SC9.C9_01ENVOP = '' 
 	AND SC9.C9_QTDLIB > 0 
 	AND SC9.C9_NFISCAL = '' 
 	AND SC9.C9_BLEST = '' 
 	AND SC9.C9_BLCRED = '' 
 	AND SC9.C9_BLOQUEI = '' 
 	AND SC9.C9_BLWMS = '' 
 	AND SC9.C9_01PEDOP <> 'XXXXXX' 
 	AND SC9.D_E_L_E_T_ = '' 
 ORDER BY 
 	SC9.C9_PEDIDO, 
 	SC9.C9_01DTFAT, 
 	SC9.C9_PRODUTO, 
 	SC9.C9_ITEM, 
 	SC9.C9_SEQUEN 
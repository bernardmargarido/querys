		SB1010 B1 
		INNER JOIN SB4010 B4 ON B4.B4_FILIAL = '  ' AND RTRIM(B4.B4_COD) = RTRIM(B1.B1_01PRODP) AND B4.B4_XENVECO = '2' AND B4.B4_XUSAECO = 'S' AND B4.D_E_L_E_T_ = '' 

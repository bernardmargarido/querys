SELECT 
	SC5.C5_NUM,SC5.C5_NOMDEST,SC5.C5_CEPDEST, 
	SC5.C5_LOGRDES,SC5.C5_NUMDEST,SC5.C5_COMPDES, 
	SC5.C5_BAIDEST,SC5.C5_CIDDEST,SC5.C5_UFDEST, 
	SC5.C5_PAISDES,SC5.C5_DDD1,SC5.C5_FONE1, 
	SC5.C5_EMISSAO,SC5.C5_TRANSP,SC5.C5_PESOL, 
	SC5.C5_PBRUTO,SC5.C5_NUMECO,SC5.C5_STATUS, 
	SA1.A1_CGC,SA1.A1_NOME,SA1.A1_CEP,SA1.A1_END, 
	SA1.A1_COMPLEM,SA1.A1_BAIRRO,SA1.A1_MUN,SA1.A1_EST, 
	SYA.YA_DESCR,SA1.A1_DDD,SA1.A1_TEL, 
	SC5.C5_FRETE +  
	(
	 SELECT SUM(C6_VALOR) FROM SC6010 WHERE C6_FILIAL = SC5.C5_FILIAL AND C6_NUM = SC5.C5_NUM
	) TOTALPV,  
	SC6.C6_ITEM,SC6.C6_PRODUTO,SC6.C6_QTDVEN,SC6.C6_PRCVEN 
FROM 
	SC5010 SC5 
	INNER JOIN  SA1010 SA1 ON SA1.A1_COD = SC5.C5_CLIENT AND SA1.A1_LOJA = SC5.C5_LOJACLI AND SA1.D_E_L_E_T_ = '' 
	INNER JOIN SC6010 SC6 ON SC6.C6_FILIAL = SC5.C5_FILIAL AND SC6.C6_NUM = SC5.C5_NUM AND SC6.D_E_L_E_T_ = '' 
	INNER JOIN SC9010 SC9 ON SC9.C9_FILIAL = SC6.C6_FILIAL AND SC9.C9_PEDIDO = SC6.C6_NUM AND SC9.C9_ITEM = SC6.C6_ITEM AND 
	SC9.C9_PRODUTO = SC6.C6_PRODUTO AND SC9.C9_BLEST = '' AND SC9.C9_BLCRED = '' AND SC9.D_E_L_E_T_ = ''
	LEFT OUTER JOIN  SYA010 SYA ON SYA.YA_CODGI = SA1.A1_PAIS AND SYA.D_E_L_E_T_ = '' 
WHERE 
	SC5.C5_FILIAL = '01' AND 
	SC5.C5_INTCNTL = '1' AND 
	SC5.C5_NOTA = '' AND 
	SC5.C5_SERIE = '' AND 
	SC5.C5_LIBEROK = 'S' AND 
	SC5.D_E_L_E_T_ = '' 
ORDER BY SC5.C5_NUM
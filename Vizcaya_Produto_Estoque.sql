SELECT 
	B2.B2_COD, 
	B1.B1_XIDSKU,
	B1.B1_DESC, 
	B1.B1_XESTMIN, 
	(B2.B2_QATU - B2.B2_RESERVA - B2.B2_QEMP - B2.B2_QACLASS - B2.B2_QEMPSA - B2.B2_QPEDVEN) SALDOB2, 
	B2.R_E_C_N_O_ RECNOSB2 
FROM 
	SB2010 B2 
	INNER JOIN SB1010 B1 ON B1.B1_FILIAL = '  ' AND B1.B1_COD = B2.B2_COD AND B1.B1_XENVECO = '2' AND B1.B1_XUSAECO = 'S' AND B1.B1_MSBLQL <> '1' AND B1.D_E_L_E_T_ = '' 	
WHERE 
	B2.B2_FILIAL = '11' AND  
	B2.B2_LOCAL = '01' AND 
	B2.B2_MSEXP = '' AND  
	B2.D_E_L_E_T_ = '' 
ORDER BY B2.B2_COD

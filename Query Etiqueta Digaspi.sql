	(SELECT AY0.AY0_DESC FROM AY0990 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT2 AND AY0.D_E_L_E_T_ = '') DESCCAT2,
	(SELECT AY0.AY0_DESC FROM AY0990 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT3 AND AY0.D_E_L_E_T_ = '') DESCCAT3,
	(SELECT AY0.AY0_DESC FROM AY0990 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT4 AND AY0.D_E_L_E_T_ = '') DESCCAT4,

SELECT 
	* 
FROM 
( 
	SELECT 
		ROW_NUMBER() OVER(ORDER BY B4_COD) RNUM,
			B4.B4_COD, 
			B4.B4_01CODMA, 
			B4.B4_XNOMPRD, 
			B4.B4_XTITULO, 
			B4.B4_XSUBTIT, 
			CAST(CAST(B4.B4_XDESCRI AS BINARY(2048)) AS VARCHAR(2048)) DESCECO, 
			CAST(CAST(B4.B4_XCARACT AS BINARY(2048)) AS VARCHAR(2048)) DESCCARA, 
			( SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = B4.B4_01CAT1 AND AY0.D_E_L_E_T_ = '' ) CAT01, 
			( SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = B4.B4_01CAT2 AND AY0.D_E_L_E_T_ = '' ) CAT02, 
			( SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = B4.B4_01CAT3 AND AY0.D_E_L_E_T_ = '' ) CAT03, 
			( SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = B4.B4_01CAT4 AND AY0.D_E_L_E_T_ = '' ) CAT04, 
			( SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = B4.B4_01CAT5 AND AY0.D_E_L_E_T_ = '' ) CAT05, 
			B4.B4_XCODENQ, 
			B4.B4_PESO, 
			B4.B4_XALTPRD, 
			B4.B4_XALTEMB, 
			B4.B4_XLARPRD, 
			B4.B4_XLAREMB, 
			B4.B4_XPROPRD, 
			B4.B4_XPROEMB, 
			B4.B4_XPRZADI, 
			B4.B4_XQTDMAX, 
			B4.B4_XTPPROD, 
			B4.B4_XPRESEN, 
			DA1.DA1_PRCVEN, 
			B4.B4_XPERSON, 
			B4.B4_XDESPER, 
			B4.B4_XISBN, 
			B4.B4_XEAN13, 
			B4.B4_XYOUTUB, 
			B4.B4_XIDFAST, 
			B4.R_E_C_N_O_ RECNOSB4 
	FROM 
		SB4010 B4
		INNER JOIN DA1010 DA1 ON DA1.DA1_FILIAL = '06' AND DA1.DA1_CODTAB = '027' AND DA1.DA1_REFGRD = B4.B4_COD AND DA1.D_E_L_E_T_ = ''
	WHERE 
		B4.B4_FILIAL = '' AND 
		B4.B4_XENVECO = '2' AND 
		B4.B4_XUSAECO = 'S' AND 
		B4.D_E_L_E_T_ = ''
)
SB4
WHERE 
RNUM BETWEEN 1 AND 300
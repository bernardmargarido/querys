SELECT
		SB1.B1_COD , 
		SB1.B1_PRV1 , 
		SB1.B1_CODBAR ,
		SB1.B1_01DREF , 
		SB4.B4_DESC , 
		(SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT1 AND AY0.D_E_L_E_T_ = '')  DESCCAT1, 
		(SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT2 AND AY0.D_E_L_E_T_ = '') DESCCAT2, 
		(SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT3 AND AY0.D_E_L_E_T_ = '') DESCCAT3, 
		(SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT4 AND AY0.D_E_L_E_T_ = '') DESCCAT4, 
		(SELECT AY0.AY0_DESC FROM AY0010 AY0 WHERE AY0.AY0_FILIAL = '' AND AY0.AY0_CODIGO = SB4.B4_01CAT5 AND AY0.D_E_L_E_T_ = '') DESCCAT5, 
		(SELECT AY2.AY2_DESCR FROM AY2010 AY2 WHERE AY2.AY2_FILIAL = '' AND AY2.AY2_CODIGO = SB4.B4_01CODMA AND AY2.D_E_L_E_T_ = '') MARCA, 
		(SELECT SBV.BV_DESCRI FROM SBV010 SBV WHERE SBV.BV_FILIAL = '' AND SBV.BV_TABELA = SB4.B4_LINHA AND SBV.BV_CHAVE = SB1.B1_01LNGRD AND D_E_L_E_T_ = '') COR, 
		(SELECT SBV.BV_DESCRI FROM SBV010 SBV WHERE SBV.BV_FILIAL = '' AND SBV.BV_TABELA = SB4.B4_COLUNA AND SBV.BV_CHAVE = SB1.B1_01CLGRD AND D_E_L_E_T_ = '') TAMANHO, 
		(SELECT SB0.B0_PRV1 FROM SB0010 SB0 WHERE SB0.B0_FILIAL = '0101' AND SB0.B0_COD = SB1.B1_COD AND SB0.D_E_L_E_T_ = '')  PRECO, 
		(SELECT SLK.LK_CODBAR FROM SLK010 SLK WHERE SLK.LK_FILIAL = '' AND SLK.LK_CODIGO = SB1.B1_COD AND SLK.D_E_L_E_T_ = '') CODBAR, 
		(SELECT AY4.AY4_VALOR FROM AY4010 AY4 WHERE AY4.AY4_FILIAL = '' AND AY4.AY4_CODCAR = AY5.AY5_CODIGO AND AY4.AY4_SEQ = AY5.AY5_SEQ AND AY4.D_E_L_E_T_ = '') ESTILO 
	FROM 
		SB4010 SB4 
	INNER JOIN SB1010 SB1 ON SB1.B1_FILIAL = '' AND SUBSTRING(SB1.B1_COD,1,9) = RTRIM(SB4.B4_COD)  AND SB1.D_E_L_E_T_ = ''
	LEFT OUTER JOIN AY5010 AY5 ON AY5.AY5_FILIAL = '' AND AY5.AY5_CODPRO = SB4.B4_COD AND AY5.AY5_CODIGO = '000014' AND AY5.D_E_L_E_T_ = '' 
	WHERE 
		SB4.B4_FILIAL = '' AND 
		SB4.B4_COD = '601030604' AND 
		SB4.D_E_L_E_T_ = '' 
	ORDER BY SB4.B4_COD 
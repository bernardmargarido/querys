SELECT FI_DTMOVTO,FI_PDV, FI_BAS18, FI_IMPDEBT
FROM SFI010
WHERE
FI_FILIAL = '03' AND
FI_DTMOVTO BETWEEN '20131101' AND '20131130' AND
FI_PDV <> '' AND
D_E_L_E_T_ = ''
ORDER BY FI_PDV,FI_DTMOVTO

SELECT FT_EMISSAO,SUM(FT_BASEICM) AS BASE, SUM(FT_VALICM) AS ICM
FROM SFT010
WHERE
FT_TIPOMOV = 'S' AND
FT_FILIAL = '03' AND
FT_EMISSAO BETWEEN '20131101' AND '20131130' AND
FT_PDV = '0002' AND
FT_DTCANC = '' AND
D_E_L_E_T_ = '' AND
FT_OBSERV = ''
GROUP BY FT_EMISSAO

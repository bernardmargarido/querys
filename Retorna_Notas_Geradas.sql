SELECT
  F1.F1_DOC,
  F1.F1_SERIE,
  F1.F1_CHVNFE,
  TO_DATE(F1.F1_EMISSAO,'yyyy-mm-dd') EMISSAO,
  
  CASE 
    WHEN F1.F1_TIPO = 'D' THEN
      ( SELECT A1.A1_CGC FROM SA1030 A1 WHERE A1.A1_COD = F1.F1_FORNECE AND A1.A1_LOJA = F1.F1_LOJA AND A1.D_E_L_E_T_ = ' ' ) 
    WHEN F1.F1_TIPO <> 'D' THEN
      ( SELECT A2.A2_CGC FROM SA2030 A2 WHERE A2.A2_COD = F1.F1_FORNECE AND A2.A2_LOJA = F1.F1_LOJA AND A2.D_E_L_E_T_ = ' ' )
  END EMITENTE,
  
  CASE 
    WHEN F1.F1_FILIAL = '01' THEN 
      '14055516000148'
    WHEN F1.F1_FILIAL = '02' THEN 
      '14055516000229'  
    WHEN F1.F1_FILIAL = '03' THEN
      '14055516000300' 
    WHEN F1.F1_FILIAL = '04' THEN
      '14055516000490'
    WHEN F1.F1_FILIAL = '05' THEN
      '14055516000229'
   END MOBLY
   
FROM 

  SF1030 F1 
WHERE
  F1.D_E_L_E_T_ = ' '
ORDER BY F1_FILIAL,F1_DOC,F1_SERIE
  
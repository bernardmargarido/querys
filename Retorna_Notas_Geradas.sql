  F2.F2_DOC,
  F2.F2_SERIE,
  F2.F2_CHVNFE,
  TO_DATE(F2.F2_EMISSAO,'yyyy-mm-dd') EMISSAO,
  CASE 
    WHEN F2.F2_FILIAL = '01' THEN 
      '14055516000148'
    WHEN F2.F2_FILIAL = '02' THEN 
      '14055516000229'  
    WHEN F2.F2_FILIAL = '03' THEN
      '14055516000300' 
    WHEN F2.F2_FILIAL = '04' THEN
      '14055516000490'
    WHEN F2.F2_FILIAL = '05' THEN
      '14055516000229'
   END EMITENTE     
FROM 
  SF2030 F2
WHERE
  F2.D_E_L_E_T_ = ' '
ORDER BY F2_FILIAL,F2_DOC,F2_SERIE


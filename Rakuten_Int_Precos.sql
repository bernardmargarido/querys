SELECT 
	B4.B4_COD,
	B4.B4_DESC,
	DA1.DA1_PRCVEN,
	DA1.DA1_PRVEC1,
	DA1.R_E_C_N_O_ RECNODA1
FROM
	DA1010 DA1
	INNER JOIN SB4010 B4 ON B4.B4_FILIAL = '' AND B4.B4_COD = DA1.DA1_REFGRD AND B4.B4_XENVECO = '2' AND B4.B4_XUSAECO = 'S' AND B4.D_E_L_E_T_ = ''
WHERE
	DA1.DA1_FILIAL = '' AND 
	DA1.DA1_CODTAB = '002' AND
	DA1.DA1_ENVECO = '1' AND 
	DA1.D_E_L_E_T_ = ''


SELECT 
	B1.B1_COD,
	B1.B1_DESC,
	DA1.DA1_PRCVEN,
	DA1.DA1_PRVEC1,
	DA1.R_E_C_N_O_ RECNODA1
FROM
	DA1010 DA1
	INNER JOIN SB1010 B1 ON B1.B1_FILIAL = '' AND B1.B1_COD = DA1.DA1_CODPRO AND B1.B1_XENVECO = '2' AND B1.D_E_L_E_T_ = ''
	INNER JOIN SB4010 B4 ON B4.B4_FILIAL = '' AND B4.B4_COD = SUBSTRING(B1.B1_COD,1,6) AND B4.B4_XENVECO = '2' AND B4.B4_XUSAECO = 'S' AND B4.D_E_L_E_T_ = ''
WHERE
	DA1.DA1_FILIAL = '' AND 
	DA1.DA1_CODTAB = '002' AND
	DA1.DA1_ENVECO = '1' AND 
	DA1.D_E_L_E_T_ = ''


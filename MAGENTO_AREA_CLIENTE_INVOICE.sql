SELECT 
	E1.E1_FILIAL,
	A1.A1_DDD,
	A1.A1_TEL,
	A1.A1_TEL2,
	A1.A1_EMLCONT,
	COALESCE(C5.C5_XNUMECO,'') C5_XNUMECO,
	E1.E1_PEDIDO,
	E1.E1_VENCREA,
	E1.E1_VALOR,
	E1.E1_NUM,
	E1.E1_PREFIXO,
	E1.E1_PARCELA 
FROM 
	SE1040 E1 WITH(NOLOCK)
	INNER JOIN SA1040 A1 WITH(NOLOCK) ON A1.A1_FILIAL = '' AND A1.A1_CGC = '54705371000106' AND A1.D_E_L_E_T_ = ''
	INNER JOIN SC5040 C5 WITH(NOLOCK) ON C5.C5_FILIAL = E1.E1_FILIAL AND C5.C5_NUM = E1.E1_PEDIDO AND E1.D_E_L_E_T_ = '' 
WHERE 
	E1.E1_CLIENTE = A1.A1_COD AND 
	E1.E1_LOJA = A1.A1_LOJA AND 
	E1.D_E_L_E_T_ = ''
ORDER BY E1.E1_NUM, E1.E1_EMISSAO
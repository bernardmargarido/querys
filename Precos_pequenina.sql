SELECT 
	B1.B1_01CODFO,
	B1.B1_01PRODP,
	B1.B1_COD,
	ISNULL(DA1.DA1_PRCVEN,0) DA1_PRCVEN
FROM 
	SB1010 B1
	LEFT OUTER JOIN DA1010 DA1 ON DA1.DA1_FILIAL = '01' AND DA1.DA1_REFGRD = B1.B1_01PRODP AND DA1.D_E_L_E_T_ = ''
WHERE 
	B1.D_E_L_E_T_ = '' 
GROUP BY B1.B1_01CODFO, B1.B1_01PRODP, B1.B1_COD ,DA1.DA1_PRCVEN
ORDER BY B1.B1_01PRODP



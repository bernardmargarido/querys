SELECT 
	F2.F2_DOC,
	F2.F2_SERIE,
	A1.A1_CGC,
	A1.A1_NOME,
	A1.A1_COD,
	A1.A1_LOJA,
	F2.F2_COND,
	F2.F2_VEND1,
	CASE 
		WHEN CHARINDEX('-capture',C5.C5_XIDORD) > 0 THEN 
			SUBSTRING(C5.C5_XIDORD,1,CHARINDEX('-capture',C5.C5_XIDORD) - 1)
		ELSE 
			C5.C5_XIDORD
	END PAY
FROM 
	SF2040 F2 (NOLOCK)
	INNER JOIN SE1040 E1 (NOLOCK) ON E1.E1_FILIAL = F2.F2_FILIAL AND E1.E1_NUM = F2.F2_DOC AND E1.E1_PREFIXO = F2.F2_SERIE AND E1.E1_BAIXA = '' AND E1.E1_SALDO > 0  AND E1.D_E_L_E_T_ = ''
	INNER JOIN SC5040 C5 (NOLOCK) ON C5.C5_FILIAL = E1.E1_FILIAL AND C5.C5_NUM = E1.E1_PEDIDO AND C5.C5_XNUMECO = '' AND C5.C5_XIDORD <> '' AND C5.D_E_L_E_T_ = ''
	INNER JOIN SA1040 A1 (NOLOCK) ON A1.A1_COD = C5.C5_CLIENTE AND A1.A1_LOJA = C5.C5_LOJACLI AND C5.D_E_L_E_T_ = ''
WHERE 
	F2.F2_FILIAL = '0404' AND 
	F2.F2_XNUMECO = '' AND 
	(F2.F2_COND LIKE '%C%' OR F2.F2_COND = 'BOL') AND
	F2.D_E_L_E_T_ = ''
GROUP BY F2.F2_DOC,F2.F2_SERIE,A1.A1_CGC,A1.A1_NOME,A1.A1_COD,A1.A1_LOJA,F2.F2_COND,F2.F2_VEND1,C5.C5_XIDORD
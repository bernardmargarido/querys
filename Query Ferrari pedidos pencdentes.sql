SELECT SC5.C5_NUM,SC5.C5_PEDCLI, SC5.C5_EMISSAO, SC5.C5_VEND1, SA3.A3_NOME, SC5.C5_TRANSP, SA4.A4_NOME, SC5.C5_DESC1, 
SC6.C6_PRODUTO, SC6.C6_DESCRI, SC6.C6_QTDVEN, SC6.C6_PRCVEN, SC6.C6_VALOR, 
SUM(SC6.C6_VALOR) AS TOTALPED, SC6.C6_DESCONT, SC6.C6_ENTREG 
FROM SC5010 SC5 
INNER JOIN SC6010 SC6 ON SC6.C6_FILIAL = SC5.C5_FILIAL AND SC6.C6_NUM = SC5.C5_NUM AND SC6.C6_DATFAT IS NOT NULL AND SC6.D_E_L_E_T_ = ' '
INNER JOIN SF4010 SF4 ON SF4.F4_FILIAL = SC5.C5_FILIAL AND SF4.F4_CODIGO = SC6.C6_TES AND SF4.F4_CF IN('5101','5109','5111','5118','5401','5405','6501','5910') AND SF4.D_E_L_E_T_ = ' '
LEFT OUTER JOIN SA3010 SA3 ON SA3.A3_COD = SC5.C5_VEND1 AND SA3.D_E_L_E_T_ = ' '
LEFT OUTER JOIN SA4010 SA4 ON SA4.A4_COD = SC5.C5_TRANSP AND SA4.D_E_L_E_T_ = ' '
WHERE
SC5.C5_PEDCLI='299584' AND 
SC5.C5_LOJACLI = '02' AND
SC5.C5_CLIENT = '001484' AND
SC5.C5_EMISSAO BETWEEN '20100101' AND '20110101' 
GROUP BY C5_NUM,C5_PEDCLI, C5_EMISSAO, C5_VEND1, A3_NOME, C5_TRANSP, A4_NOME, C5_DESC1, C6_PRODUTO, C6_DESCRI, C6_QTDVEN, C6_PRCVEN, C6_VALOR, C6_DESCONT, C6_ENTREG 
ORDER BY C5_EMISSAO, C5_VEND1, C5_TRANSP, C5_DESC1  

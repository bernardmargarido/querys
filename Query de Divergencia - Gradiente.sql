-- Notas de Transferencia
SELECT SF2.F2_DOC,SF2.F2_SERIE,SF2.F2_EMISSAO,SF2.F2_VEICUL1, 
SF2.F2_CLIENTE,SF2.F2_LOJA,SF2.F2_TRANSP,SF2.F2_TIPO, 
SF2.R_E_C_N_O_ RECNOSF2,SC5.R_E_C_N_O_ RECNOSC5, 
SD2.D2_COD,SD2.D2_QUANT,SD2.D2_UM,SD2.D2_LOTECTL 
FROM 
SF2010 SF2 
INNER JOIN SD2010 SD2 ON SD2.D2_FILIAL = SF2.F2_FILIAL AND SD2.D2_DOC = SF2.F2_DOC AND SD2.D2_SERIE = SF2.F2_SERIE AND SD2.D_E_L_E_T_ = '' 
INNER JOIN SC5010 SC5 ON SC5.C5_FILIAL = SD2.D2_FILIAL AND SC5.C5_NUM = SD2.D2_PEDIDO AND SC5.C5_CLIENTE = SD2.D2_CLIENTE AND SC5.C5_LOJACLI = SD2.D2_LOJA AND SC5.D_E_L_E_T_ = '' 
WHERE 
SF2.F2_FILIAL = '02' AND 
SF2.F2_MSEXP = '' AND 
SF2.F2_TEGINT IN('1','2') AND 
SF2.F2_CLIENTE = '888888' AND 
SF2.F2_LOJA = '01' AND  
SF2.D_E_L_E_T_ = '' 
ORDER BY SF2.F2_DOC 

-- Relatorio de Divergencia Transferencia
SELECT SD2.D2_DOC,SD2.D2_SERIE,SD2.D2_CLIENTE,
SD2.D2_LOJA,SD2.D2_EMISSAO,SD2.D2_ITEM,SD2.D2_COD,
SD2.D2_QUANT,AYT.AYT_QTDENT,AYT.AYT_QTDDIV
FROM SD2010 SD2
INNER JOIN AYT010 AYT ON AYT.AYT_FILIAL = ' ' AND AYT.AYT_DOC = SD2.D2_DOC AND AYT.AYT_SERIE = SD2.D2_SERIE AND SD2.D2_ITEM = AYT.AYT_ITNF AND  AYT.D_E_L_E_T_ = ''
INNER JOIN SF2010 SF2 ON SF2.F2_FILIAL = '02' AND  SF2.F2_DOC = SD2.D2_DOC AND SF2.F2_SERIE = SD2.D2_SERIE AND SF2.F2_TEGINT = '3' AND SF2.D_E_L_E_T_ = ''
WHERE 
SD2.D2_FILIAL = '02' AND 
SD2.D2_DOC BETWEEN '' AND 'ZZZZZZZZZ' AND 
SD2.D2_SERIE BETWEEN '' AND 'ZZZ' AND 
SD2.D2_EMISSAO BETWEEN '' AND '20120430' AND
SD2.D_E_L_E_T_ = '' 
ORDER BY SD2.D2_DOC

-- Notas de Saida venda
SELECT SF2.F2_DOC,SF2.F2_SERIE,SF2.F2_EMISSAO,SF2.F2_VEICUL1, 
SF2.F2_CLIENTE,SF2.F2_LOJA,SF2.F2_TRANSP,SF2.F2_TIPO, 
SC5.C5_NUM,SC5.C5_EMISSAO,SC5.C5_CLIENTE,SC5.C5_LOJACLI,
SF2.R_E_C_N_O_ RECNOSF2,SC5.R_E_C_N_O_ RECNOSC5
FROM 
SF2010 SF2 
INNER JOIN SC5010 SC5 ON SC5.C5_FILIAL = SF2.F2_FILIAL AND SC5.C5_NOTA = SF2.F2_DOC AND SC5.C5_SERIE = SF2.F2_SERIE AND SC5.D_E_L_E_T_ = '' 
WHERE 
SF2.F2_FILIAL = '02' AND 
SF2.F2_MSEXP = '' AND 
SF2.F2_TEGINT IN('1','2') AND 
SF2.F2_CLIENTE + SF2.F2_LOJA <> '88888801' AND 
--SF2.F2_LOJA <> '01' AND  
SF2.D_E_L_E_T_ = '' 
ORDER BY SF2.F2_DOC 


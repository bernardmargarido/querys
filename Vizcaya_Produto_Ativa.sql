SELECT
	B1.B1_XIDPROD,
	B1.B1_XIDSKU,
	B1.R_E_C_N_O_ RECNOSB1
FROM
	SB1010 B1
	INNER JOIN DA1010 DA1 ON DA1.DA1_FILIAL = '  ' AND DA1.DA1_CODPRO = B1.B1_COD AND DA1.DA1_CODTAB = 'ECO' AND DA1.DA1_ENVECO = '2' AND DA1.D_E_L_E_T_ = ''
	INNER JOIN SB2010 B2 ON B2.B2_FILIAL = '11' AND B2.B2_COD = B1.B1_COD AND B2.B2_LOCAL = '01' AND B2.B2_MSEXP <> '' AND B2.D_E_L_E_T_ = ''
WHERE
	B1.B1_FILIAL = '' AND
	B1.B1_XUSAECO = 'S' AND 
	B1.B1_XENVECO = '2' AND 
	B1.B1_XENVSKU = '2' AND 
	B1.B1_ATIVO	= '1' AND 
	B1.D_E_L_E_T_ = ''
ORDER BY B1.B1_COD



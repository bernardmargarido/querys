SELECT SF2.F2_DOC,SF2.F2_SERIE,SF2.F2_PBRUTO,SF2.F2_VOLUME1,SF2.F2_TPFRETE,SF2.F2_CLIENTE,SF2.F2_LOJA, 
SF2.F2_EMISSAO,SF2.F2_VALBRUT,SF2.F2_VALMERC,SF2.F2_CHVNFE, 
--SD2.D2_TES,SD2.D2_CF, 
SC5.C5_NUM,SC5.C5_NOMDEST,SC5.C5_CPFCART,SC5.C5_TIPOLOG,SC5.C5_LOGRDES,SC5.C5_NUMDEST,SC5.C5_COMPDES, 
SC5.C5_REFERE,SC5.C5_BAIDEST,SC5.C5_CIDDEST,SC5.C5_UFDEST,SC5.C5_PAISDES,SC5.C5_CEPDEST,SC5.C5_DDD1,  
SC5.C5_FONE1,SC5.C5_FONE2,SC5.C5_CELULAR
--SF4.F4_TEXTO 
FROM 
AYZ010 AYZ 
INNER JOIN SF2010 SF2 ON SF2.F2_FILIAL = '02' AND SF2.F2_DOC = AYZ.AYZ_NOTA AND SF2.F2_SERIE = AYZ.AYZ_SERIE AND SF2.D_E_L_E_T_ = '' 
INNER JOIN SC5010 SC5 ON SC5.C5_FILIAL = '02' AND SC5.C5_NUMECO = SF2.F2_PEDECOM AND SC5.D_E_L_E_T_ = '' 
WHERE 
AYZ.AYZ_FILIAL = '02' AND 
AYZ.AYZ_CHAVE = '' AND  
AYZ.AYZ_DTSAID = '' AND 
AYZ.AYZ_HRSAID = '' AND 
AYZ.AYZ_TPFLAG = 'N' AND 
AYZ.D_E_L_E_T_ = '' 
ORDER BY AYZ.AYZ_NOTA 


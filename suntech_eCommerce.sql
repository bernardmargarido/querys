ZA3.R_E_C_N_O_ AS RECNOZA3, ZA1.R_E_C_N_O_ AS RECNOZA1
FROM ZA3010 ZA3
INNER JOIN ZA1010 ZA1 ON ZA1.ZA1_FILIAL = ZA3.ZA3_FILIAL AND ZA1.ZA1_CODIGO = ZA3.ZA3_CODCAR AND ZA1.D_E_L_E_T_ = ''
WHERE
ZA3.ZA3_FILIAL = '  ' AND ZA3.ZA3_ENVECO = '1' AND ZA3.D_E_L_E_T_ = ''
ORDER BY ZA3_CODCAR, ZA3_CODCAT


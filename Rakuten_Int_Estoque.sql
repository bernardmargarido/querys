SELECT 
	B2.B2_COD,
	B4.B4_COD,
	B4.B4_XESTMIN,
	B2.R_E_C_N_O_ RECNOSB2
FROM
	SB2010 B2
	INNER JOIN SB1010 B1 ON B1.B1_FILIAL = ' ' AND B1.B1_COD = B2.B2_COD AND B1.B1_XENVECO = '2' AND B1.D_E_L_E_T_ = ''	
	INNER JOIN SB4010 B4 ON B4.B4_FILIAL = ' ' AND B4.B4_COD = SUBSTRING(B2.B2_COD,1,6) AND B4.B4_XENVECO = '2' AND B4.D_E_L_E_T_ = ''
WHERE
	B2.B2_FILIAL = '03' AND 
	B2.B2_LOCAL = '01' AND
	B2.B2_MSEXP = ' ' AND 
	B2.D_E_L_E_T_ = ' ' 
ORDER BY B2.B2_COD 
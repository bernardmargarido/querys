SELECT AY3.AY3_CODIGO,AY4.AY4_SEQ, 
CAST(CAST(AY4.AY4_VLRECO AS BINARY) AS VARCHAR) VLRECO, 
CAST(CAST(AY3.AY3_DESCEC AS BINARY) AS VARCHAR) CARDESC, 
AY4.AY4_OPER, AY4.R_E_C_N_O_ RECNOAY4, SB4.B4_CAT1,SB4.B4_CAT2, 
SB4.B4_CAT3,SB4.B4_CAT4,SB4.B4_CAT5 
FROM 
AY4010 AY4 
INNER JOIN  AY3010 AY3 ON AY3.AY3_FILIAL = ' ' AND AY3_CODIGO = AY4.AY4_CODCAR AND AY3.D_E_L_E_T_ = '' 
INNER JOIN  AY5010 AY5 ON AY5.AY5_FILIAL = ' ' AND AY5_CODIGO = AY4.AY4_CODCAR AND AY5.D_E_L_E_T_ = '' 
INNER JOIN  SB4010 SB4 ON SB4.B4_FILIAL = ' ' AND SB4.B4_COD = AY5.AY5_CODPRO AND SB4.B4_USAECO = '1' AND SB4.B4_ENVECO = '2' AND SB4.B4_STATECO = '1' AND SB4.D_E_L_E_T_ = '' 
WHERE 
AY4.AY4_FILIAL = ' ' AND  
AY4.AY4_STATEC = '1' AND 
AY4.AY4_ENVECO = '2' AND 
AY4.D_E_L_E_T_ = ' ' 


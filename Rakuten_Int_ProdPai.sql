SELECT 
	B4.B4_COD,
	(
		SELECT
			DISTINCT(B1.B1_PRODPAI)
		FROM 
			SB1010 B1
		WHERE
			SUBSTRING(B1.B1_COD,1,6) = B4.B4_COD AND
			B1.D_E_L_E_T_ = ''
		) CODPAI,
	B4.B4_01CODMA,
	B4.B4_XNOMPRD,
	B4.B4_XTITULO,
	B4.B4_XSUBTIT,
	CAST(CAST(B4.B4_XDESCRI AS BINARY(1024)) AS VARCHAR(1024)) DESCECO ,
	CAST(CAST(B4.B4_XCARACT AS BINARY(1024)) AS VARCHAR(1024)) DESCCARA ,
	B4.B4_XCODENQ,
	B4.B4_PESO,
	B4.B4_XPESBRU,
	B4.B4_XALTPRD,
	B4.B4_XALTEMB,
	B4.B4_XLARPRD,
	B4.B4_XLAREMB,
	B4.B4_XPROPRD,
	B4.B4_XPROEMB,
	B4.B4_XPRZADI,
	B4.B4_XQTDMAX,
	B4.B4_STATUS,
	B4.B4_XTPPROD,
	B4.B4_XPRESEN,
	DA1.DA1_PRCVEN,
	DA1.DA1_PRVEC1,
	B4.B4_XPERSON,
	B4.B4_XDESPER,
	B4.B4_XISBN,
	B4.B4_XEAN13,
	B4.B4_XYOUTUB,
	B4.R_E_C_N_O_ RECNOSB4
FROM
	SB4010 B4
	INNER JOIN DA1010 DA1 ON DA1.DA1_FILIAL = '' AND DA1.DA1_CODTAB = '002' AND DA1.DA1_REFGRD = B4.B4_COD AND DA1.D_E_L_E_T_ = ''
WHERE
	B4.B4_FILIAL = '' AND 
	B4.B4_XENVECO = '1' AND
	B4.B4_XUSAECO = 'S' AND
	B4.D_E_L_E_T_ = ''


